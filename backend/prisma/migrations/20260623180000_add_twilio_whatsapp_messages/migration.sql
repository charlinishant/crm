CREATE TABLE `WhatsAppMessage` (
  `id` INTEGER NOT NULL AUTO_INCREMENT,
  `leadId` INTEGER NOT NULL,
  `userId` INTEGER NULL,
  `direction` VARCHAR(191) NOT NULL,
  `from` VARCHAR(191) NOT NULL,
  `to` VARCHAR(191) NOT NULL,
  `body` TEXT NOT NULL,
  `provider` VARCHAR(191) NOT NULL DEFAULT 'twilio',
  `providerMessageId` VARCHAR(191) NULL,
  `status` VARCHAR(191) NOT NULL DEFAULT 'queued',
  `errorCode` VARCHAR(191) NULL,
  `errorMessage` TEXT NULL,
  `sentAt` DATETIME(3) NULL,
  `deliveredAt` DATETIME(3) NULL,
  `readAt` DATETIME(3) NULL,
  `createdAt` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  `updatedAt` DATETIME(3) NOT NULL,
  UNIQUE INDEX `WhatsAppMessage_providerMessageId_key`(`providerMessageId`),
  INDEX `WhatsAppMessage_leadId_createdAt_idx`(`leadId`, `createdAt`),
  INDEX `WhatsAppMessage_userId_idx`(`userId`),
  INDEX `WhatsAppMessage_status_idx`(`status`),
  PRIMARY KEY (`id`),
  CONSTRAINT `WhatsAppMessage_leadId_fkey` FOREIGN KEY (`leadId`) REFERENCES `Lead`(`id`) ON DELETE RESTRICT ON UPDATE CASCADE,
  CONSTRAINT `WhatsAppMessage_userId_fkey` FOREIGN KEY (`userId`) REFERENCES `User`(`id`) ON DELETE SET NULL ON UPDATE CASCADE
) DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
